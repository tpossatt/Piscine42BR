int		ft_is_prime(int nb)
{
	int aux;
	int i;

	aux = nb / 2;
	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	while (i <= aux)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
